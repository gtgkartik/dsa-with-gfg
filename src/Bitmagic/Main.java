package Bitmagic;

public class Main {
    public static void main(String[] args) {
        int a = 3 ;
        int b = 6 ;

        // Bit wise OR
        System.out.println(a|b);

        // Bit wise AND
        System.out.println(a&b);

        //Bit wise xor
        System.out.println(a^b);

        //Bit wise not
        System.out.println(~a);

        //Bit
    }
}
